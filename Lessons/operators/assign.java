public class assign {
    public static void main(String args[]){
        int a=9;
        System.out.println(a+=10);
        a=9;
        System.out.println(a-=10);
        a=9;
        System.out.println(a*=10);
        a=9;
        System.out.println(a/=10);
        a=9;
        System.out.println(a=10);
    }
}
